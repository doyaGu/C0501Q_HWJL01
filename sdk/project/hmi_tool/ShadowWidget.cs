        public class StringListConverter : TypeConverter
        {
            // Convert from a string.
            public override object ConvertFrom(ITypeDescriptorContext context, System.Globalization.CultureInfo culture, object value)
            {
                if (value.GetType() == typeof(string))
                {
                    return value;
                }
                else
                {
                    return base.ConvertFrom(context, culture, value);
                }
            }

            public override bool
            GetStandardValuesSupported(ITypeDescriptorContext context)
            {
                return true; // display drop
            }
            public override bool
            GetStandardValuesExclusive(ITypeDescriptorContext context)
            {
                return true; // drop-down vs combo
            }
            public override StandardValuesCollection
            GetStandardValues(ITypeDescriptorContext context)
            {
                // note you can also look at context etc to build list
                return new StandardValuesCollection(NameCreationService.names.ToArray());
            }
        }

        [TypeConverter(typeof(StringListConverter))]
        public String Target { get; set; }

        private bool hided = false;
        public bool Hided
        {
            get
            {
                return hided;
            }

            set
            {
                if (value)
                    Hide();
                else
                    Show();

                hided = value;
            }
        }

        public ITUWidget CreateITUWidget()
        {
            ITUShadow shadow = new ITUShadow();

            shadow.name = this.Name;

            PropertyDescriptorCollection properties = TypeDescriptor.GetProperties(this);
            shadow.visible = (bool)properties["Visible"].GetValue(this);

            shadow.active = false;
            shadow.dirty = false;
            shadow.alpha = 255;
            shadow.rect.x = this.Location.X;
            shadow.rect.y = this.Location.Y;
            shadow.rect.width = this.Size.Width;
            shadow.rect.height = this.Size.Height;
            shadow.color.alpha = this.Alpha;
            shadow.color.red = 0;
            shadow.color.green = 0;
            shadow.color.blue = 0;
            shadow.bound.x = 0;
            shadow.bound.y = 0;
            shadow.bound.width = 0;
            shadow.bound.height = 0;

            shadow.targetName = this.Target;

            return shadow;
        }

