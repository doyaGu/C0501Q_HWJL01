unsigned int get32(unsigned char *ul)
{
    unsigned char *b;
    unsigned int ret;

    b = (MMP_UINT8 *)ul;

    ret = *b++;
    ret <<= 8;
    ret |= *b++;
    ret <<= 8;
    ret |= *b++;
    ret <<= 8;
    ret |= *b;

    return ret;
}

unsigned short get16(unsigned char *ul)
{
    unsigned char *b;
    unsigned short ret;

    b = (unsigned char *)ul;

    ret = *b++;
    ret <<= 8;
    ret |= *b;

    return ret;
}

unsigned char *put32(unsigned char *cp, unsigned int x)
{
    *cp++ = x >> 24;
    *cp++ = x >> 16;
    *cp++ = x >> 8;
    *cp++ = x;
    
    return cp;
} 

unsigned char *put16(unsigned char *cp, unsigned short x)
{
    *cp++ = x >> 8;
    *cp++ = x;

    return cp;
} 
